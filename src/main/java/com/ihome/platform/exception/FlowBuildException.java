package com.ihome.platform.exception;

public class FlowBuildException extends AbstractException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6520362551529949871L;

	public FlowBuildException() {
		super();
	}

	public FlowBuildException(String message) {
		super(message);
	}

	public FlowBuildException(String message, Throwable throwable) {
		super(message, throwable);
	}

	public FlowBuildException(Throwable throwable) {
		super(throwable);
	}

}
